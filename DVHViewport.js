  seriesSource(data) {
    let dataArr = [];
    for (const key in data) {
      if (data.hasOwnProperty(key)) {
        const el = data[key];
        var rgbStr =
          'rgb(' + el.color[0] + ',' + el.color[1] + ',' + el.color[2] + ')';
        dataArr.push({
          type: 'line',
          color: rgbStr,
          smooth: true,
          seriesLayoutBy: 'row',
        });
      }
    }
    return dataArr;
  }

